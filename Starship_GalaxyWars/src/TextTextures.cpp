Texture GetCharTexture(char ch)
{
	if (ch < 32) 
	{
		return {nullptr, 0, 0, 0};
	}


	// The textures for text chars are all from ascii, 
	// but it's been delta'd a bit down since we don't need control characters.
	// 32 is normally the first value that we care about in ascii, but its the first idx texture.
	uint8_t textureIdx = ch - 32;
	const uint16_t* textureData = myBitmapallArray[textureIdx];

	Texture result = {textureData, 7*9, 7, 9}; 
	return result;

}
